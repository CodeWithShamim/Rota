/** Deterministic gradient avatar for a wallet address. */
export function AddressAvatar({ address, className = "h-5 w-5" }: IconProps & { address: string }) {
  const seed = parseInt(address.slice(2, 10), 16) || 0;
  const h1 = seed % 360;
  const h2 = (h1 + 60 + (seed % 120)) % 360;
  return (
    <span
      aria-hidden="true"
      className={`inline-block shrink-0 rounded-full ${className}`}
      style={{ background: `linear-gradient(135deg, hsl(${h1} 70% 55%), hsl(${h2} 70% 45%))` }}
    />
  );
}
